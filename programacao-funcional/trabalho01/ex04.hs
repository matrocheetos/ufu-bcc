lista = [1..20]

gera1 :: [Int]
gera1 = [ x^2 | x <- lista, even x, x>3, x<11 ]
-- [16,36,64,100]

gera2 :: [(Int,Int)]
gera2 = [ (x,y) | x <- lista, x<=5, y <- lista, y>=x, y<=(3*x) ]
-- [(1,1),(1,2),(1,3),
-- (2,2),(2,3),(2,4),(2,5),(2,6),
-- (3,3),(3,4),(3,5),(3,6),(3,7),(3,8),(3,9),
